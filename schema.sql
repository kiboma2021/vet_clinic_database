/* vet_clinic database. */

CREATE TABLE animals(
    id INT GENERATED BY DEFAULT AS IDENTITY,,
    name CHAR(50) NOT NULL,
    date_of_birth DATE NOT NULL,
    escape_attempts INT,
    neutered BOOLEAN,
    weight_kg DECIMAL
);

ALTER TABLE animals ADD species VARCHAR(255);

/* owners table */
CREATE TABLE owners (
  id INT GENERATED BY DEFAULT AS IDENTITY,
  full_name VARCHAR(100),
  age INT,
  PRIMARY KEY(id)
);

/* species table */
CREATE TABLE species (
  id INT GENERATED BY DEFAULT AS IDENTITY,
  name VARCHAR(100),
  PRIMARY KEY(id)
);

ALTER TABLE animals DROP COLUMN species;
ALTER TABLE animals ADD COLUMN species_id INT;
ALTER TABLE animals ADD FOREIGN KEY (species_id) REFERENCES species(id)
ON DELETE CASCADE;

ALTER TABLE animals ADD COLUMN owner_id INT;
ALTER TABLE animals ADD FOREIGN KEY (owner_id) REFERENCES owners(id)
ON DELETE CASCADE;

CREATE TABLE vets(
    id INT GENERATED ALWAYS AS IDENTITY, 
    name VARCHAR(255) NOT NULL, 
    age INT NOT NULL, 
    date_of_graduation DATE NOT NULL, 

    PRIMARY KEY (id)
);

CREATE TABLE specializations(
    id INT GENERATED ALWAYS AS IDENTITY,
    species_id INT , 
    vets_id INT, 
);

ALTER TABLE specializations ADD FOREIGN KEY (species_id) REFERENCES species (id);
ALTER TABLE specializations ADD FOREIGN KEY (vets_id) REFERENCES vets (id);

CREATE TABLE visits (
    id INT GENERATED ALWAYS AS IDENTITY, 
    animals_id INT, 
    vets_id INT, 
    date_of_visit DATE, 
);

ALTER TABLE visits ADD FOREIGN KEY (vets_id) REFERENCES vets (id);
ALTER TABLE visits ADD FOREIGN KEY (animals_id) REFERENCES animals (id);